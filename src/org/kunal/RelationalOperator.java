package org.kunal;

public class RelationalOperator {
	public static void main(String args[]) {
		int a = 20, b = 10;
		boolean condition = true;
		
		System.out.println(a == b);
		
		System.out.println(a >= b);
		
		System.out.println(a <= b);
		
		System.out.println(a < b);
		
		System.out.println(a > b);
		
		System.out.println(a != b);
		
		System.out.println(condition == true);
		
	}
}
