package org.kunal;

public class UnaryOperator {
	public static void main(String args[]) {
		
		int a = 3;
		boolean condition = true;
		
		a++;
		System.out.println(a);
		
		--a;
		System.out.println(a);
		
		++a;
		System.out.println(a);
		
		a--;
		System.out.println(a);
		
		System.out.println(!condition);
	}
}
