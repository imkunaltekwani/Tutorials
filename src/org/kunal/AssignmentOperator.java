package org.kunal;

public class AssignmentOperator {
	public static void main(String args[]) {
		
		int a = 5, b = 7, c = 9, d = 11, e = 13;
		
		
		a += 2;
		b -= 2;
		c *= 2;
		d /= 3;
		e %= 2;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		
	}
}
